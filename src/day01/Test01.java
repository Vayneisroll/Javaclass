package day01;
// 한 줄 주석(comments) : ctrl + shift + C
/*
 *  실행 : ctrl + F11
 *  이름바꾸기 : F2
 */
public class Test01 { // Test01 클래스(==부품) 영역 
//	자바는 클래스 단위로 프로그래밍함.
//	클래스 영역 안에서만 코드를 작성한다.
	
	public static void main(String[] args){ // 메인메소드(함수)
		System.out.println("방가방가~~~");
	}
	/*
	 * < 메인메소드란? >
	 * 형식 : 
	 *  public static void main(String[] args){ .. }
	 * 
	 * - 프로그램의 주인공 
	 * - 실행 시, JVM이 가장 먼저 메인메소드를 찾아서, 그 내용을 실행
	 * - 프로그램 시작점
	 * - 메인메소드가 끝나면(즉, 닫는 중괄호(})를 만나면)
	 *   자동으로 프로그램 종료 
	 * - 메인메소드가 있는 클래스 == 프로그램
	 * 
	 * < 표준출력(sysout) >
	 *  System.out.println();
	 * - 소괄호 내부의 데이터를 console(cmd)에 출력
	 *   ** 세미콜론(;) : 명령 1개의 마침표 역할
	 */
}


















